//A loop that turns 10 times and write the number of each iteration
#include <iostream>
using namespace std;
int main()
{
    //Declare variables
    int i;
    //Loop for and display iteration number
    for (i=1; i<=10; i++)
    {
        cout<<"This is iteration "<<i<<endl;
    }
    //Display i after the last iteration
    cout<<endl<<"At the end of the loop i is equal to "<<i<<endl;
}






