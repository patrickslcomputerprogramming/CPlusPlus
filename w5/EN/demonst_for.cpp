//Demonstrate 10 iterations using a for loop
#include <iostream>
using namespace std;
int main()
{
    //Declare variables
    int i;
    //Operate 10 iterations
    for (i=1; i<=10; i++) {
        cout<<"Iteration # "<<i<<endl;
    }
    //Display i after the 10th iteration
    cout<<"\nOutside the loop. \nAfter the last iterations, i is equal to "<<i<<endl;
    return 0;
}




