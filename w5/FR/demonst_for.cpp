//Demonstrate 10 iterations using a for loop
#include <iostream>
using namespace std;
int main()
{
    //Declare variables
    int i;
    //Operate 10 iterations
    for (i=1; i<=10; i++) {
        cout<<"A L'INTERIEUR DE LA BOUCLE. ITERATION # "<<i<<endl;
    }
    //Display i after the 10th iteration
    //Display outside
    cout<<"\nA L'EXTERIEUR DE LA BOUCLE.. \nAPRES LA DERNIERE ITERATION, i EST EGAL A  "<<i<<endl;
}




